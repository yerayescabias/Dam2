package paagbi.model;

public class Nori {
    String Izena;
    int Adina;
    
    public Nori(String izena, int adina) {
        Izena = izena;
        Adina = adina;
    }
    public String getIzena() {
        return Izena;
    }
    public void setIzena(String izena) {
        Izena = izena;
    }
    public int getAdina() {
        return Adina;
    }
    public void setAdina(int adina) {
        Adina = adina;
    }
}
